_LAYERNORM_TEMPLATE = '''\
"""Auto-generated by Lunara — Layer Norm (two-pass)"""
import triton
import triton.language as tl


@triton.jit
def {name}(X_ptr, W_ptr, B_ptr, Y_ptr,
            N, eps,
            BLOCK: tl.constexpr):
    row  = tl.program_id(0)
    cols = tl.arange(0, BLOCK)
    mask = cols < N

    x = tl.load(X_ptr + row * N + cols, mask=mask, other=0.0).to(tl.float32)
    mean = tl.sum(x, axis=0) / N
    var  = tl.sum((x - mean) * (x - mean), axis=0) / N
    x_norm = (x - mean) / tl.math.sqrt(var + eps)

    w = tl.load(W_ptr + cols, mask=mask, other=1.0)
    b = tl.load(B_ptr + cols, mask=mask, other=0.0)
    y = x_norm * w + b
    tl.store(Y_ptr + row * N + cols, y.to(tl.{dtype}), mask=mask)


def {name}_launch(X, weight, bias, eps=1e-5):
    import torch
    assert X.is_cuda
    shape = X.shape
    X_2d = X.view(-1, shape[-1])
    M, N = X_2d.shape
    Y = torch.empty_like(X_2d)
    BLOCK = triton.next_power_of_2(N)
    {name}[(M,)](X_2d, weight, bias, Y, N, eps,
                  BLOCK=BLOCK, num_warps={num_warps})
    return Y.view(shape)
'''


def emit_layernorm(spec: KernelSpec) -> str:
    return _LAYERNORM_TEMPLATE.format(name=spec.name,
                                       num_warps=spec.tile.num_warps,
                                       dtype=spec.dtype)


