_SOFTMAX_TEMPLATE = '''\
"""Auto-generated by Lunara — Fused online Softmax"""
import triton
import triton.language as tl


@triton.jit
def {name}(X_ptr, Y_ptr, M, N,
            BLOCK: tl.constexpr):
    """One CTA per row; online max+sum in a single pass."""
    row = tl.program_id(0)
    cols = tl.arange(0, BLOCK)
    mask = cols < N
    x = tl.load(X_ptr + row * N + cols, mask=mask, other=-float("inf"))
    x_max = tl.max(x, axis=0)
    x_exp = tl.math.exp(x - x_max)
    x_sum = tl.sum(x_exp, axis=0)
    y = x_exp / x_sum
    tl.store(Y_ptr + row * N + cols, y, mask=mask)


def {name}_launch(X):
    import torch
    assert X.is_cuda
    M, N = X.shape
    Y = torch.empty_like(X)
    BLOCK = triton.next_power_of_2(N)
    {name}[(M,)](X, Y, M, N, BLOCK=BLOCK, num_warps={num_warps})
    return Y
'''


def emit_softmax(spec: KernelSpec) -> str:
    return _SOFTMAX_TEMPLATE.format(name=spec.name,
                                     num_warps=spec.tile.num_warps)


